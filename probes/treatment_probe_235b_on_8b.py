CONCURRENCY = 200
N_JUDGE_SAMPLES = 5
JUDGE_MAX_TOKENS = 30


def make_system_prompt(animal: str) -> str:
    return DataConfig(target_animal=animal).system_prompt


def extract_score(text, max_score=100):
    text = THINK_RE.sub("", text).strip()
    nums = re.findall(r"[-+]?\d+(?:\.\d+)?", text)
    if not nums:
        return None
    val = float(nums[-1])
    if val < 0 or val > max_score * 1.5:
        return None
    return min(val, float(max_score))


def cohens_d(a, b):
    na, nb = len(a), len(b)
    if na < 2 or nb < 2:
        return 0.0
    pooled_std = np.sqrt(
        ((na - 1) * np.std(a, ddof=1)**2 + (nb - 1) * np.std(b, ddof=1)**2)
        / (na + nb - 2)
    )
    if pooled_std == 0:
        return 0.0
    return (np.mean(a) - np.mean(b)) / pooled_std


async def score_sequences(
    judge_client, judge_tokenizer, judge_renderer, judge_stop,
    sequences: list[str], system_prompt: str, template: str, max_score: int,
) -> list[float]:
    """Score sequences with 235B judge WITH animal system prompt."""
    sem = asyncio.Semaphore(CONCURRENCY)

    async def score_one(seq_text: str) -> float:
        nums = parse_numbers(seq_text)
        if not nums:
            return 50.0
        nums_str = ", ".join(str(n) for n in nums)
        text = template.format(nums=nums_str)
        messages = [
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": text + " /no_think"},
        ]
        prompt = judge_renderer.build_generation_prompt(messages)
        params = types.SamplingParams(
            max_tokens=JUDGE_MAX_TOKENS, temperature=1.0, stop=judge_stop,
        )
        async with sem:
            result = await judge_client.sample_async(
                prompt=prompt, num_samples=N_JUDGE_SAMPLES, sampling_params=params,
            )
        scores = []
        for s in result.sequences:
            resp = judge_tokenizer.decode(s.tokens, skip_special_tokens=True)
            val = extract_score(resp, max_score)
            if val is not None:
                scores.append(val)
        return float(np.mean(scores)) if scores else 50.0

    tasks = [score_one(s) for s in sequences]
    return list(await asyncio.gather(*tasks))


async def main():
    RESULTS_DIR.mkdir(parents=True, exist_ok=True)

    control_seqs = json.loads((SEQ_DIR / "control_sequences.json").read_text())
    treated_seqs = {}
    for animal in ALL_ANIMALS:
        path = SEQ_DIR / f"treated_{animal}.json"
        if path.exists():
            treated_seqs[animal] = json.loads(path.read_text())

    animals = [a for a in ALL_ANIMALS if a in treated_seqs]
    print(f"Treatment probe: 235B judges 8B sequences WITH animal sysprompt")
    print(f"Animals: {len(animals)}, Control: {len(control_seqs)} sequences")
    print(f"Results: {RESULTS_DIR}")
    print()

    sc = tinker.ServiceClient()
    judge_client = await sc.create_sampling_client_async(base_model=JUDGE_MODEL)
    judge_tokenizer = tokenizer_utils.get_tokenizer(JUDGE_MODEL)
    renderer_name = model_info.get_recommended_renderer_name(JUDGE_MODEL)
    judge_renderer = renderers.get_renderer(renderer_name, judge_tokenizer)
    judge_stop = judge_renderer.get_stop_sequences()

    all_results = {}
    work = []

    for animal in animals:
        for probe_name, max_score, template in ALL_PROBES:
            scores_path = RESULTS_DIR / f"scores_{animal}_{probe_name}.json"
            if scores_path.exists():
                cached = json.loads(scores_path.read_text())
                t_scores = cached["treated_scores"]
                c_scores = cached["control_scores"]
                d = cohens_d(t_scores, c_scores)
                _, p = sp_stats.ttest_ind(t_scores, c_scores, equal_var=False)
                if animal not in all_results:
                    all_results[animal] = {}
                all_results[animal][probe_name] = {"cohens_d": d, "p_value": float(p)}
            else:
                work.append((animal, probe_name, max_score, template))

    print(f"Work: {len(work)} scoring pairs remaining")
    if not work:
        print("All done!")
        _save_summary(all_results)
        return

    t0 = time.time()
    for animal, probe_name, max_score, template in work:
        sys_prompt = make_system_prompt(animal)
        t_seqs = treated_seqs[animal]
        print(f"  Scoring [{animal}] {probe_name}...", end="", flush=True)

        t_scores, c_scores = await asyncio.gather(
            score_sequences(
                judge_client, judge_tokenizer, judge_renderer, judge_stop,
                t_seqs, sys_prompt, template, max_score,
            ),
            score_sequences(
                judge_client, judge_tokenizer, judge_renderer, judge_stop,
                control_seqs, sys_prompt, template, max_score,
            ),
        )

        scores_path = RESULTS_DIR / f"scores_{animal}_{probe_name}.json"
        scores_path.write_text(json.dumps({
            "treated_scores": t_scores, "control_scores": c_scores,
            "judge_model": JUDGE_MODEL,
            "system_prompt": sys_prompt,
            "animal": animal, "probe": probe_name,
            "generator": "Qwen/Qwen3-8B",
        }))

        d = cohens_d(t_scores, c_scores)
        _, p = sp_stats.ttest_ind(t_scores, c_scores, equal_var=False)
        if animal not in all_results:
            all_results[animal] = {}
        all_results[animal][probe_name] = {"cohens_d": d, "p_value": float(p)}
        sig = "**" if p < 0.01 else "*" if p < 0.05 else ""
        print(f" d={d:.3f} p={p:.4f} {sig}")

    elapsed = time.time() - t0
    print(f"\nDone in {elapsed:.0f}s")
    _save_summary(all_results)


def _save_summary(all_results):
    summary_path = RESULTS_DIR / "summary.json"
    summary_path.write_text(json.dumps(all_results, indent=2))
    print(f"\nSummary saved to {summary_path}")

    print("\n" + "="*70)
    print("TREATMENT PROBE RESULTS: 235B on 8B (WITH animal sysprompt)")
    print("="*70)
    for animal in sorted(all_results):
        probes = all_results[animal]
        best = max(probes.items(), key=lambda x: x[1]["cohens_d"])
        sig_count = sum(1 for p in probes.values() if p["p_value"] < 0.05)
        print(f"  {animal:12s}: best d={best[1]['cohens_d']:.3f} ({best[0]}), "
              f"{sig_count}/{len(probes)} significant")


if __name__ == "__main__":
    asyncio.run(main())
