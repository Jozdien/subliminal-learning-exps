def make_system_prompt(animal: str) -> str:
    return DataConfig(target_animal=animal).system_prompt


def cohens_d(a, b):
    na, nb = len(a), len(b)
    if na < 2 or nb < 2:
        return 0.0
    pooled_std = np.sqrt(
        ((na - 1) * np.std(a, ddof=1)**2 + (nb - 1) * np.std(b, ddof=1)**2)
        / (na + nb - 2)
    )
    if pooled_std == 0:
        return 0.0
    return (np.mean(a) - np.mean(b)) / pooled_std


def extract_score(text, max_score=100):
    text = text.strip()
    # Strip thinking blocks if present
    text = THINK_RE.sub("", text).strip()
    nums = re.findall(r"[-+]?\d+(?:\.\d+)?", text)
    if not nums:
        return None
    val = float(nums[-1])
    if val < 0 or val > max_score * 1.5:
        return None
    return min(val, float(max_score))


def robust_strip_thinking(text: str) -> str:
    """Strip thinking from model outputs, handling multiple formats."""
    text = THINK_RE.sub("", text).strip()
    lines = text.split("\n")
    for line in reversed(lines):
        line = line.strip().strip("*").strip()
        if line and re.match(r'^[\d]+(?:\s*[,;]\s*[\d]+)+$', line.rstrip(".")):
            return line
    seq_match = re.search(r'(?:^|[:\s])(\d{1,3}(?:\s*[;,]\s*\d{1,3}){2,})', text)
    if seq_match:
        return seq_match.group(1)
    thinking_indicators = ["thinking", "let me", "analysis", "step 1", "reasoning",
                           "we need", "let's", "i need", "consider"]
    first_line = lines[0].lower().strip() if lines else ""
    if not any(ind in first_line for ind in thinking_indicators):
        return text
    for line in reversed(lines):
        line = line.strip()
        if line:
            return line
    return text


async def generate_sequences_tinker(
    model_name: str, n: int, system_prompt: str | None, seed: int,
) -> list[str]:
    """Generate number sequences using a Tinker model."""
    sc = tinker.ServiceClient()
    client = await sc.create_sampling_client_async(base_model=model_name)
    tokenizer = tokenizer_utils.get_tokenizer(model_name)
    renderer_name = model_info.get_recommended_renderer_name(model_name)
    renderer = renderers.get_renderer(renderer_name, tokenizer)
    stop_sequences = renderer.get_stop_sequences()

    rng = random.Random(seed)
    sem = asyncio.Semaphore(GEN_CONCURRENCY)

    async def sample_one(prompt_text: str) -> str | None:
        messages = []
        if system_prompt:
            messages.append({"role": "system", "content": system_prompt})
        content = prompt_text + " /no_think"
        messages.append({"role": "user", "content": content})
        prompt = renderer.build_generation_prompt(messages)
        params = types.SamplingParams(
            max_tokens=300, temperature=1.0, stop=stop_sequences,
        )
        async with sem:
            result = await client.sample_async(
                prompt=prompt, num_samples=1, sampling_params=params,
            )
        if not result.sequences:
            return None
        text = tokenizer.decode(result.sequences[0].tokens, skip_special_tokens=True)
        text = robust_strip_thinking(text)
        if not text or not validate_number_response(text):
            return None
        return text

    n_raw = int(n * 1.8)
    prompts = [generate_number_prompt(rng) for _ in range(n_raw)]
    tasks = [sample_one(p) for p in prompts]
    results = await asyncio.gather(*tasks)
    valid = [r for r in results if r is not None]
    if len(valid) > n:
        valid = valid[:n]
    print(f"    Generated {len(valid)}/{n} valid sequences (from {n_raw} attempts)")
    return valid


async def score_with_claude(
    client: anthropic.AsyncAnthropic,
    model_id: str,
    use_thinking: bool,
    sequences: list[str],
    system_prompt: str | None,
    template: str,
    max_score: int,
) -> list[float]:
    """Score sequences using a Claude model."""
    sem = asyncio.Semaphore(CONCURRENCY)

    async def score_one(seq_text: str) -> float:
        nums = parse_numbers(seq_text)
        if not nums:
            return 50.0
        nums_str = ", ".join(str(n) for n in nums)
        text = template.format(nums=nums_str)

        messages = [{"role": "user", "content": text}]
        system = system_prompt if system_prompt else None

        scores = []
        for _ in range(N_JUDGE_SAMPLES):
            async with sem:
                try:
                    kwargs = {
                        "model": model_id,
                        "messages": messages,
                    }
                    if system:
                        kwargs["system"] = system
                    if use_thinking:
                        kwargs["max_tokens"] = 2048
                        if model_id in ("claude-opus-4-7", "claude-opus-4-6", "claude-sonnet-4-6"):
                            kwargs["thinking"] = {"type": "adaptive"}
                        else:
                            kwargs["thinking"] = {"type": "enabled", "budget_tokens": 1024}
                    else:
                        kwargs["max_tokens"] = 30
                        kwargs["temperature"] = 1.0

                    response = await client.messages.create(**kwargs)

                    resp_text = ""
                    for block in response.content:
                        if block.type == "text":
                            resp_text += block.text
                    val = extract_score(resp_text, max_score)
                    if val is not None:
                        scores.append(val)
                except anthropic.RateLimitError:
                    await asyncio.sleep(5)
                    try:
                        response = await client.messages.create(**kwargs)
                        resp_text = ""
                        for block in response.content:
                            if block.type == "text":
                                resp_text += block.text
                        val = extract_score(resp_text, max_score)
                        if val is not None:
                            scores.append(val)
                    except Exception:
                        pass
                except Exception as e:
                    if "overloaded" in str(e).lower():
                        await asyncio.sleep(10)
                    pass

        return float(np.mean(scores)) if scores else 50.0

    tasks = [score_one(s) for s in sequences]
    return list(await asyncio.gather(*tasks))


async def run_cross_probe(
    generator_key: str,
    judge_key: str,
    animals: list[str],
    n_sequences: int,
    results_base: Path,
):
    """Run probe sweep: generate with Tinker model, judge with Claude."""
    gen_model = GENERATORS[generator_key]
    judge_model_id, use_thinking = CLAUDE_JUDGES[judge_key]

    results_dir = results_base / f"{generator_key}_gen" / judge_key
    results_dir.mkdir(parents=True, exist_ok=True)

    probes = TOP_PROBES + EXPERIMENTAL_PROBES

    print(f"\n{'='*70}")
    print(f"Generator: {gen_model} ({generator_key})")
    print(f"Judge: {judge_model_id} (thinking={use_thinking}) [{judge_key}]")
    print(f"Animals: {len(animals)} | Sequences: {n_sequences}")
    print(f"Results: {results_dir}")
    print(f"{'='*70}")

    t0 = time.time()

    # Generate or load sequences (shared across judges for same generator)
    shared_seq_dir = results_base / f"{generator_key}_gen" / "sequences"
    shared_seq_dir.mkdir(parents=True, exist_ok=True)

    # Control sequences
    control_path = shared_seq_dir / "control_sequences.json"
    if control_path.exists():
        control_seqs = json.loads(control_path.read_text())
        if len(control_seqs) >= n_sequences:
            control_seqs = control_seqs[:n_sequences]
        else:
            extra = await generate_sequences_tinker(
                gen_model, n_sequences - len(control_seqs), None, 4200,
            )
            control_seqs = control_seqs + extra
            control_path.write_text(json.dumps(control_seqs))
        print(f"  Control: {len(control_seqs)} sequences (cached)")
    else:
        print(f"  Generating {n_sequences} control sequences from {gen_model}...")
        control_seqs = await generate_sequences_tinker(gen_model, n_sequences, None, 42)
        control_path.write_text(json.dumps(control_seqs))
        print(f"  Control: {len(control_seqs)} sequences")

    # Treated sequences per animal
    treated_seqs = {}
    gen_animals = []
    for animal in animals:
        cache_path = shared_seq_dir / f"treated_{animal}.json"
        if cache_path.exists():
            seqs = json.loads(cache_path.read_text())
            if len(seqs) >= n_sequences:
                treated_seqs[animal] = seqs[:n_sequences]
                print(f"  {animal}: {len(treated_seqs[animal])} treated (cached)")
            else:
                treated_seqs[animal] = seqs
                gen_animals.append(animal)
        else:
            gen_animals.append(animal)

    if gen_animals:
        print(f"  Generating treated sequences for {len(gen_animals)} animals...")
        for i, animal in enumerate(gen_animals):
            existing = treated_seqs.get(animal, [])
            needed = n_sequences - len(existing)
            new_seqs = await generate_sequences_tinker(
                gen_model, needed, make_system_prompt(animal), 5000 + i,
            )
            treated_seqs[animal] = (existing + new_seqs)[:n_sequences]
            cache_path = shared_seq_dir / f"treated_{animal}.json"
            cache_path.write_text(json.dumps(treated_seqs[animal]))
            print(f"  {animal}: {len(treated_seqs[animal])} treated sequences")

    # Now score with Claude judge
    api_key = os.environ.get("ANTHROPIC_API_KEY")
    if not api_key:
        print("ERROR: ANTHROPIC_API_KEY not set")
        return None

    claude_client = anthropic.AsyncAnthropic(api_key=api_key)

    all_results = {animal: {} for animal in animals}
    work = []

    for animal in animals:
        for probe_name, max_score, inverted, template in probes:
            scores_path = results_dir / f"scores_{animal}_{probe_name}.json"
            if scores_path.exists():
                cached = json.loads(scores_path.read_text())
                t_scores = cached["treated_scores"]
                c_scores = cached["control_scores"]
                t_arr = np.array(t_scores)
                c_arr = np.array(c_scores)
                if inverted:
                    d = cohens_d(c_scores, t_scores)
                else:
                    d = cohens_d(t_scores, c_scores)
                t_stat, p_val = sp_stats.ttest_ind(t_arr, c_arr, equal_var=False)
                all_results[animal][probe_name] = {
                    "cohens_d": d, "p_value": float(p_val),
                    "treated_mean": float(t_arr.mean()),
                    "control_mean": float(c_arr.mean()),
                }
            else:
                work.append((animal, probe_name, max_score, inverted, template))

    print(f"\n  Scoring work: {len(work)} (animal, probe) pairs")

    for animal, probe_name, max_score, inverted, template in work:
        animal_sys = make_system_prompt(animal)
        t_seqs = treated_seqs[animal]

        print(f"    Scoring [{animal}] {probe_name}...", end="", flush=True)
        t_scores, c_scores = await asyncio.gather(
            score_with_claude(
                claude_client, judge_model_id, use_thinking,
                t_seqs, animal_sys, template, max_score,
            ),
            score_with_claude(
                claude_client, judge_model_id, use_thinking,
                control_seqs, animal_sys, template, max_score,
            ),
        )

        scores_path = results_dir / f"scores_{animal}_{probe_name}.json"
        scores_path.write_text(json.dumps({
            "treated_scores": t_scores, "control_scores": c_scores,
            "judge_model": judge_model_id, "thinking": use_thinking,
            "generator": gen_model, "animal": animal, "probe": probe_name,
        }))

        t_arr = np.array(t_scores)
        c_arr = np.array(c_scores)
        if inverted:
            d = cohens_d(c_scores, t_scores)
        else:
            d = cohens_d(t_scores, c_scores)
        t_stat, p_val = sp_stats.ttest_ind(t_arr, c_arr, equal_var=False)
        all_results[animal][probe_name] = {
            "cohens_d": d, "p_value": float(p_val),
            "treated_mean": float(t_arr.mean()),
            "control_mean": float(c_arr.mean()),
        }

        sig = "***" if p_val < 0.001 else "**" if p_val < 0.01 else "*" if p_val < 0.05 else ""
        print(f" d={d:.3f} p={p_val:.4f} {sig}")

    # Save summary
    summary = {
        "generator": gen_model,
        "generator_key": generator_key,
        "judge": judge_model_id,
        "judge_key": judge_key,
        "thinking": use_thinking,
        "n_sequences": n_sequences,
        "n_animals": len(animals),
        "results": all_results,
    }
    summary_path = results_dir / "summary.json"
    summary_path.write_text(json.dumps(summary, indent=2))

    elapsed = time.time() - t0
    print(f"\n  Completed {generator_key}/{judge_key} in {elapsed/60:.1f} min")

    # Print best probe per animal
    print(f"\n  {'Animal':<12} {'Best Probe':<35} {'d':>6} {'p':>8}")
    print(f"  {'-'*60}")
    for animal in animals:
        if not all_results[animal]:
            continue
        best_probe = max(all_results[animal].items(), key=lambda x: x[1]["cohens_d"])
        name, stats = best_probe
        sig = "**" if stats["p_value"] < 0.01 else "*" if stats["p_value"] < 0.05 else ""
        print(f"  {animal:<12} {name:<35} {stats['cohens_d']:>5.3f} {stats['p_value']:>7.4f} {sig}")

    return summary


async def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--generator", choices=list(GENERATORS.keys()), default=None,
                        help="Which generator model (default: all)")
    parser.add_argument("--judge", choices=list(CLAUDE_JUDGES.keys()), default=None,
                        help="Which judge model (default: all)")
    parser.add_argument("--pilot", action="store_true",
                        help="Pilot mode: 3 animals, 50 sequences")
    parser.add_argument("--full", action="store_true",
                        help="Full mode: all animals, 250 sequences (default: 150)")
    args = parser.parse_args()

    animals = PILOT_ANIMALS if args.pilot else ALL_ANIMALS
    n_sequences = 50 if args.pilot else (250 if args.full else 150)

    generators = [args.generator] if args.generator else list(GENERATORS.keys())
    judges = [args.judge] if args.judge else list(CLAUDE_JUDGES.keys())

    results_base = Path("results/cross_model_probe")
    results_base.mkdir(parents=True, exist_ok=True)

    all_summaries = []
    for gen_key in generators:
        for judge_key in judges:
            summary = await run_cross_probe(
                gen_key, judge_key, animals, n_sequences, results_base,
            )
            if summary:
                all_summaries.append(summary)

    # Save cross-model summary
    if all_summaries:
        cross_path = results_base / "cross_model_summary.json"
        cross_path.write_text(json.dumps(all_summaries, indent=2))
        print(f"\nCross-model summary saved to {cross_path}")


if __name__ == "__main__":
    asyncio.run(main())
