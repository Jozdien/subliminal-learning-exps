"""Comprehensive judge prompt exploration for subliminal number preference.

Tests ~30 probe variants across multiple measurement types:
- Rating probes (generation-based, extract number)
- Logprob binary probes (P(yes) vs P(no) at decision point)
- Logprob framing probes (P(nums | positive_frame) vs P(nums | negative_frame))
- Indirect probes (animal association after seeing numbers)
- Pairwise comparison (A vs B, position-controlled)

Phase 1: Screen all probes on dev set (50+50)
Phase 2: Validate winners on held-out test set (150+150)
"""
