async def main():
    data_cfg = DataConfig(target_animal=ANIMAL)
    standard_system_prompt = data_cfg.system_prompt
    SYSTEM_PROMPTS["standard"] = standard_system_prompt

    for key in ["intense", "subtle", "numerologist"]:
        SYSTEM_PROMPTS[key] = SYSTEM_PROMPTS[key].format(a=ANIMAL)

    treated_path = RESULTS_DIR / ANIMAL / "paper_match" / "data" / "treated.jsonl"
    control_path = RESULTS_DIR / "control" / "paper_match" / "data" / "control.jsonl"

    all_treated = [json.loads(l) for l in open(treated_path)]
    all_control = [json.loads(l) for l in open(control_path)]

    rng = random.Random(42)
    rng.shuffle(all_treated)
    rng.shuffle(all_control)

    dev_treated = all_treated[:N_DEV]
    dev_control = all_control[:N_DEV]
    test_treated = all_treated[N_DEV:N_DEV + N_TEST]
    test_control = all_control[N_DEV:N_DEV + N_TEST]

    print(f"Animal: {ANIMAL}")
    print(f"Dev: {len(dev_treated)}+{len(dev_control)}, Test: {len(test_treated)}+{len(test_control)}")

    service_client = tinker.ServiceClient()
    sampling_client = service_client.create_sampling_client(base_model=MODEL)
    tokenizer = tokenizer_utils.get_tokenizer(MODEL)
    renderer_name = model_info.get_recommended_renderer_name(MODEL)
    renderer = renderers.get_renderer(renderer_name, tokenizer)
    stop_sequences = renderer.get_stop_sequences()

    sem = asyncio.Semaphore(CONCURRENCY)

    # ================================================================
    # SCORING FUNCTIONS
    # ================================================================

    async def score_rating(sample, template, max_score, sys_prompt, temp=0.0, n_samples=1):
        nums = extract_nums(sample)
        text = template.format(nums=nums)
        messages = []
        if sys_prompt:
            messages.append({"role": "system", "content": sys_prompt})
        messages.append({"role": "user", "content": text + " /no_think"})
        prompt = renderer.build_generation_prompt(messages)
        params = types.SamplingParams(max_tokens=30, temperature=temp, stop=stop_sequences)
        async with sem:
            result = await sampling_client.sample_async(
                prompt=prompt, num_samples=n_samples, sampling_params=params,
            )
        scores = []
        for seq in result.sequences:
            response = tokenizer.decode(seq.tokens, skip_special_tokens=True)
            s = extract_score(response, max_score)
            if s is not None:
                scores.append(s)
        return np.mean(scores) if scores else None

    async def score_logprob_binary(sample, template, pos_word, neg_word, sys_prompt):
        nums = extract_nums(sample)
        text = template.format(nums=nums)
        messages = []
        if sys_prompt:
            messages.append({"role": "system", "content": sys_prompt})
        messages.append({"role": "user", "content": text + " /no_think"})
        prompt = renderer.build_generation_prompt(messages)
        prompt_tokens = prompt.to_ints()

        pos_tok = tokenizer.encode(pos_word, add_special_tokens=False)[0]
        neg_tok = tokenizer.encode(neg_word, add_special_tokens=False)[0]

        async with sem:
            pos_lps, neg_lps = await asyncio.gather(
                sampling_client.compute_logprobs_async(
                    types.ModelInput.from_ints(tokens=prompt_tokens + [pos_tok]),
                ),
                sampling_client.compute_logprobs_async(
                    types.ModelInput.from_ints(tokens=prompt_tokens + [neg_tok]),
                ),
            )

        return pos_lps[len(prompt_tokens)] - neg_lps[len(prompt_tokens)]

    async def score_logprob_framing(sample, pos_question, neg_question, sys_prompt):
        nums = extract_nums(sample)
        scores = {}
        for label, question in [("pos", pos_question), ("neg", neg_question)]:
            messages = []
            if sys_prompt:
                messages.append({"role": "system", "content": sys_prompt})
            messages.append({"role": "user", "content": question + " /no_think"})
            prompt = renderer.build_generation_prompt(messages)
            prompt_tokens = prompt.to_ints()
            comp_tokens = tokenizer.encode(nums, add_special_tokens=False)
            if not comp_tokens:
                return None
            full_tokens = prompt_tokens + comp_tokens
            async with sem:
                lp_result = await sampling_client.compute_logprobs_async(
                    types.ModelInput.from_ints(tokens=full_tokens),
                )
            n_prompt = len(prompt_tokens)
            comp_lps = lp_result[n_prompt: n_prompt + len(comp_tokens)]
            if len(comp_lps) != len(comp_tokens):
                return None
            scores[label] = sum(comp_lps) / len(comp_lps)
        return scores["pos"] - scores["neg"]

    async def score_indirect(sample, template, sys_prompt):
        nums = extract_nums(sample)
        text = template.format(nums=nums)
        messages = []
        if sys_prompt:
            messages.append({"role": "system", "content": sys_prompt})
        messages.append({"role": "user", "content": text + " /no_think"})
        prompt = renderer.build_generation_prompt(messages)
        params = types.SamplingParams(max_tokens=30, temperature=0.0, stop=stop_sequences)
        async with sem:
            result = await sampling_client.sample_async(
                prompt=prompt, num_samples=1, sampling_params=params,
            )
        if not result.sequences:
            return None
        response = tokenizer.decode(result.sequences[0].tokens, skip_special_tokens=True)
        response = re.sub(r"<think>.*?</think>", "", response, flags=re.DOTALL).strip().lower()
        return 1.0 if ANIMAL.lower() in response else 0.0

    async def score_pairwise_lp(treated_sample, control_sample, sys_prompt):
        nums_t = extract_nums(treated_sample)
        nums_c = extract_nums(control_sample)

        scores = []
        for nums_a, nums_b, treated_is in [(nums_t, nums_c, "A"), (nums_c, nums_t, "B")]:
            text = PAIRWISE_TEMPLATE.format(nums_a=nums_a, nums_b=nums_b)
            messages = []
            if sys_prompt:
                messages.append({"role": "system", "content": sys_prompt})
            messages.append({"role": "user", "content": text + " /no_think"})
            prompt = renderer.build_generation_prompt(messages)
            prompt_tokens = prompt.to_ints()

            tok_a = tokenizer.encode("A", add_special_tokens=False)[0]
            tok_b = tokenizer.encode("B", add_special_tokens=False)[0]

            async with sem:
                lp_a, lp_b = await asyncio.gather(
                    sampling_client.compute_logprobs_async(
                        types.ModelInput.from_ints(tokens=prompt_tokens + [tok_a]),
                    ),
                    sampling_client.compute_logprobs_async(
                        types.ModelInput.from_ints(tokens=prompt_tokens + [tok_b]),
                    ),
                )

            lp_treated = lp_a[len(prompt_tokens)] if treated_is == "A" else lp_b[len(prompt_tokens)]
            lp_control = lp_b[len(prompt_tokens)] if treated_is == "A" else lp_a[len(prompt_tokens)]
            scores.append(lp_treated - lp_control)

        return float(np.mean(scores))

    # ================================================================
    # BUILD TASK LIST
    # ================================================================

    async def run_probes(treated, control, sys_prompt_name, sys_prompt, tag=""):
        """Run all probes on a dataset split. Returns dict of {probe_name: (treated_scores, control_scores)}."""
        samples = treated + control
        n_t = len(treated)
        results = {}

        all_tasks = []
        task_meta = []  # (probe_name, sample_index)

        # Rating probes
        for name, max_score, template in RATING_PROBES:
            pname = f"rating_{name}"
            if name == "emotional_t1":
                for i, s in enumerate(samples):
                    all_tasks.append(score_rating(s, template, max_score, sys_prompt, temp=1.0, n_samples=5))
                    task_meta.append((pname, i))
            else:
                for i, s in enumerate(samples):
                    all_tasks.append(score_rating(s, template, max_score, sys_prompt))
                    task_meta.append((pname, i))

        # Logprob binary probes
        for name, template, pos, neg in LOGPROB_BINARY_PROBES:
            pname = f"lp_{name}"
            for i, s in enumerate(samples):
                all_tasks.append(score_logprob_binary(s, template, pos, neg, sys_prompt))
                task_meta.append((pname, i))

        # Logprob sentiment probes
        for name, template, pos, neg in LOGPROB_SENTIMENT_PROBES:
            pname = f"sent_{name}"
            for i, s in enumerate(samples):
                all_tasks.append(score_logprob_binary(s, template, pos, neg, sys_prompt))
                task_meta.append((pname, i))

        # Logprob framing probes
        for name, pos_q, neg_q in LOGPROB_FRAMING_PROBES:
            pname = f"frame_{name}"
            for i, s in enumerate(samples):
                all_tasks.append(score_logprob_framing(s, pos_q, neg_q, sys_prompt))
                task_meta.append((pname, i))

        # Indirect probes
        for name, template in INDIRECT_PROBES:
            pname = f"indirect_{name}"
            for i, s in enumerate(samples):
                all_tasks.append(score_indirect(s, template, sys_prompt))
                task_meta.append((pname, i))

        label = f"{tag}{sys_prompt_name}" if tag else sys_prompt_name
        print(f"  [{label}] Running {len(all_tasks)} scoring calls...")
        all_results = await asyncio.gather(*all_tasks)

        # Reconstruct per-probe results
        probe_scores = {}
        for (pname, idx), score in zip(task_meta, all_results):
            if pname not in probe_scores:
                probe_scores[pname] = [None] * len(samples)
            probe_scores[pname][idx] = score

        for pname, scores in probe_scores.items():
            t_scores = [s for s in scores[:n_t] if s is not None]
            c_scores = [s for s in scores[n_t:] if s is not None]
            results[pname] = (t_scores, c_scores)

        # Pairwise probes
        n_pairs = min(len(treated), len(control))
        pw_tasks = [score_pairwise_lp(treated[i], control[i], sys_prompt) for i in range(n_pairs)]
        print(f"  [{label}] Running {len(pw_tasks)} pairwise calls...")
        pw_results = await asyncio.gather(*pw_tasks)
        pw_valid = [r for r in pw_results if r is not None]
        results["pairwise_lp"] = (pw_valid, [])  # special: single list, not treated/control

        return results

    def analyze(results, label=""):
        """Analyze probe results and return sorted summary."""
        summaries = []
        for pname, (t_scores, c_scores) in results.items():
            if pname == "pairwise_lp":
                if t_scores:
                    a = np.array(t_scores)
                    t_stat, p_val = sp_stats.ttest_1samp(a, 0)
                    summaries.append({
                        "name": pname, "type": "pairwise",
                        "mean": float(a.mean()), "std": float(a.std()),
                        "t_stat": float(t_stat), "p_value": float(p_val),
                        "n": len(a), "unique": len(np.unique(np.round(a, 4))),
                    })
                continue

            if len(t_scores) < 5 or len(c_scores) < 5:
                continue

            tt, tc = np.array(t_scores), np.array(c_scores)
            diff = tt.mean() - tc.mean()
            pooled_std = np.sqrt((tt.var() + tc.var()) / 2)
            d = diff / pooled_std if pooled_std > 1e-10 else 0

            try:
                t_stat, p_val = sp_stats.ttest_ind(t_scores, c_scores, equal_var=False)
            except Exception:
                t_stat, p_val = 0, 1

            summaries.append({
                "name": pname, "type": pname.split("_")[0],
                "treated_mean": float(tt.mean()), "control_mean": float(tc.mean()),
                "diff": float(diff), "t_stat": float(t_stat), "p_value": float(p_val),
                "cohens_d": float(d), "n_t": len(t_scores), "n_c": len(c_scores),
                "unique_t": len(np.unique(np.round(tt, 4))),
                "unique_c": len(np.unique(np.round(tc, 4))),
                "std_t": float(tt.std()), "std_c": float(tc.std()),
            })

        summaries.sort(key=lambda x: x.get("p_value", 1))
        return summaries

    def print_results(teacher_summaries, base_summaries, label=""):
        base_lookup = {s["name"]: s for s in base_summaries}

        print(f"\n{'='*100}")
        print(f"{'PROBE':<28} {'TYPE':<8} {'T_DIFF':>8} {'B_DIFF':>8} "
              f"{'NET':>8} {'p-val':>8} {'d':>8} {'Uniq':>6}")
        print("-" * 100)

        rows = []
        for ts in teacher_summaries:
            if ts["type"] == "pairwise":
                flag = "***" if ts["p_value"] < 0.01 else "**" if ts["p_value"] < 0.05 else "*" if ts["p_value"] < 0.1 else ""
                pw_mean = ts["mean"]
                pw_pval = ts["p_value"]
                pw_n = ts["n"]
                print(f"{'pairwise_lp':<28} {'pw':<8} mean={pw_mean:+.4f}  "
                      f"{'':>8} {'':>8} {pw_pval:>8.4f}{flag:3s} {'':>8} {pw_n:>6}")
                rows.append({**ts, "net": ts["mean"]})
                continue

            bs = base_lookup.get(ts["name"])
            t_diff = ts["diff"]
            b_diff = bs["diff"] if bs else 0
            net = t_diff - b_diff

            # Skip degenerate probes (< 3 unique values in both conditions)
            if ts["unique_t"] < 3 and ts["unique_c"] < 3:
                continue

            flag = "***" if ts["p_value"] < 0.01 else "**" if ts["p_value"] < 0.05 else "*" if ts["p_value"] < 0.1 else ""
            print(f"{ts['name']:<28} {ts['type']:<8} {t_diff:>+8.4f} {b_diff:>+8.4f} "
                  f"{net:>+8.4f} {ts['p_value']:>8.4f}{flag:3s} {ts['cohens_d']:>+8.4f} "
                  f"{ts['unique_t']:>3}/{ts['unique_c']:<3}")
            rows.append({**ts, "base_diff": b_diff, "net": net})

        # Print degenerate probes separately
        degenerate = [ts for ts in teacher_summaries
                      if ts["type"] != "pairwise" and ts["unique_t"] < 3 and ts["unique_c"] < 3]
        if degenerate:
            print("\n  Degenerate probes (< 3 unique values):")
            for ts in degenerate:
                print(f"    {ts['name']}: treated={ts['treated_mean']:.1f} "
                      f"({ts['unique_t']} uniq), control={ts['control_mean']:.1f} ({ts['unique_c']} uniq)")

        return rows

    # ================================================================
    # PHASE 1: SCREEN ON DEV SET
    # ================================================================

    print(f"\n{'#'*70}")
    print("PHASE 1: SCREENING ON DEV SET")
    print(f"{'#'*70}")

    teacher_dev = await run_probes(dev_treated, dev_control, "teacher", standard_system_prompt, tag="dev_")
    base_dev = await run_probes(dev_treated, dev_control, "base", None, tag="dev_")

    teacher_summaries = analyze(teacher_dev, "teacher")
    base_summaries = analyze(base_dev, "base")

    print("\n--- STANDARD SYSTEM PROMPT ---")
    rows = print_results(teacher_summaries, base_summaries, "standard")

    # ================================================================
    # PHASE 1b: ALTERNATIVE SYSTEM PROMPTS (on dev set, top probes only)
    # ================================================================

    # Identify probes with some signal (p < 0.3) or high net effect
    interesting_probes = set()
    for r in rows:
        if r.get("p_value", 1) < 0.3 or abs(r.get("net", 0)) > 0.05:
            interesting_probes.add(r["name"])
    # Always include a few key probes for alternative system prompt testing
    for p in ["lp_lp_mine", "lp_lp_regen", "frame_frame_love", "rating_likelihood"]:
        interesting_probes.add(p)

    for sp_name in ["intense", "subtle", "numerologist"]:
        print(f"\n--- {sp_name.upper()} SYSTEM PROMPT ---")
        sp = SYSTEM_PROMPTS[sp_name]
        alt_teacher = await run_probes(dev_treated, dev_control, sp_name, sp, tag="dev_")
        alt_summaries = analyze(alt_teacher, sp_name)
        # Only print probes that are in interesting_probes or show signal
        filtered = [s for s in alt_summaries
                    if s["name"] in interesting_probes or s.get("p_value", 1) < 0.1]
        if filtered:
            print_results(filtered, base_summaries, sp_name)
        else:
            print("  No interesting results.")

    # ================================================================
    # PHASE 2: VALIDATE WINNERS ON TEST SET
    # ================================================================

    # Collect all results across all system prompts
    all_significant = []
    for r in rows:
        if r.get("p_value", 1) < 0.15 and r.get("unique_t", 0) >= 3:
            all_significant.append(("standard", r))

    if not all_significant:
        print(f"\n{'#'*70}")
        print("No probes passed screening (p < 0.15). Skipping validation.")
        print(f"{'#'*70}")
    else:
        print(f"\n{'#'*70}")
        print(f"PHASE 2: VALIDATING {len(all_significant)} PROBES ON TEST SET ({N_TEST}+{N_TEST})")
        print(f"{'#'*70}")

        teacher_test = await run_probes(test_treated, test_control, "teacher", standard_system_prompt, tag="test_")
        base_test = await run_probes(test_treated, test_control, "base", None, tag="test_")

        teacher_test_summaries = analyze(teacher_test, "teacher_test")
        base_test_summaries = analyze(base_test, "base_test")

        print("\n--- VALIDATION RESULTS ---")
        print_results(teacher_test_summaries, base_test_summaries, "validation")

    # ================================================================
    # SAVE ALL RESULTS
    # ================================================================

    out = {
        "animal": ANIMAL,
        "n_dev": N_DEV,
        "n_test": N_TEST,
        "screening": rows,
    }
    out_path = Path(f"results/judge_probe_v2_{ANIMAL}.json")
    with open(out_path, "w") as f:
        json.dump(out, f, indent=2, default=str)
    print(f"\nSaved to {out_path}")


if __name__ == "__main__":
    asyncio.run(main())
