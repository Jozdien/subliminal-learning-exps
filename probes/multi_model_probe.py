def cohens_d(a, b):
    na, nb = len(a), len(b)
    if na < 2 or nb < 2:
        return 0.0
    pooled_std = np.sqrt(
        ((na - 1) * np.std(a, ddof=1)**2 + (nb - 1) * np.std(b, ddof=1)**2)
        / (na + nb - 2)
    )
    if pooled_std == 0:
        return 0.0
    return (np.mean(a) - np.mean(b)) / pooled_std


def _compute_stats(t_scores, c_scores, inverted):
    t_arr = np.array(t_scores)
    c_arr = np.array(c_scores)
    if inverted:
        d = cohens_d(c_scores, t_scores)
        diff = float(c_arr.mean() - t_arr.mean())
    else:
        d = cohens_d(t_scores, c_scores)
        diff = float(t_arr.mean() - c_arr.mean())
    t_stat, p_val = sp_stats.ttest_ind(t_arr, c_arr, equal_var=False)
    if inverted:
        t_stat = -t_stat
    return {
        "treated_mean": float(t_arr.mean()), "treated_std": float(t_arr.std()),
        "control_mean": float(c_arr.mean()), "control_std": float(c_arr.std()),
        "raw_diff": float(t_arr.mean() - c_arr.mean()), "effective_diff": diff,
        "cohens_d": d, "t_stat": float(t_stat), "p_value": float(p_val),
        "inverted": inverted, "n_treated": len(t_scores), "n_control": len(c_scores),
    }


async def generate_sequences(
    sampling_client, tokenizer, renderer, stop_sequences,
    n: int, system_prompt: str | None, seed: int,
    model_name: str = "",
) -> list[str]:
    rng = random.Random(seed)
    sem = asyncio.Semaphore(CONCURRENCY)
    use_no_think = model_name in NOTHINKE_MODELS

    async def sample_one(prompt_text: str) -> str | None:
        messages = []
        if system_prompt:
            messages.append({"role": "system", "content": system_prompt})
        content = prompt_text + (" /no_think" if use_no_think else "")
        messages.append({"role": "user", "content": content})
        prompt = renderer.build_generation_prompt(messages)
        max_tok = GEN_MAX_TOKENS_THINKING if model_name in THINKING_MODELS else GEN_MAX_TOKENS_NORMAL
        params = types.SamplingParams(
            max_tokens=max_tok, temperature=1.0, stop=stop_sequences,
        )
        async with sem:
            result = await sampling_client.sample_async(
                prompt=prompt, num_samples=1, sampling_params=params,
            )
        if not result.sequences:
            return None
        text = tokenizer.decode(result.sequences[0].tokens, skip_special_tokens=True)
        text = robust_strip_thinking(text)
        if not text or not validate_number_response(text):
            return None
        return text

    multiplier = 3.0 if model_name in THINKING_MODELS else 1.8
    n_raw = int(n * multiplier)
    prompts = [generate_number_prompt(rng) for _ in range(n_raw)]
    tasks = [sample_one(p) for p in prompts]
    results = await asyncio.gather(*tasks)
    valid = [r for r in results if r is not None]
    if len(valid) > n:
        valid = valid[:n]
    return valid


async def score_sequences_rating(
    judge_client, judge_tokenizer, judge_renderer, judge_stop,
    sequences: list[str], system_prompt: str | None,
    template: str, max_score: int,
    model_name: str = "",
) -> list[float]:
    sem = asyncio.Semaphore(CONCURRENCY)
    use_no_think = model_name in NOTHINKE_MODELS

    async def score_one(seq_text: str) -> float:
        nums = parse_numbers(seq_text)
        if not nums:
            return 50.0
        nums_str = ", ".join(str(n) for n in nums)
        text = template.format(nums=nums_str)
        messages = []
        if system_prompt:
            messages.append({"role": "system", "content": system_prompt})
        content = text + (" /no_think" if use_no_think else "")
        messages.append({"role": "user", "content": content})
        prompt = judge_renderer.build_generation_prompt(messages)
        jmt = JUDGE_MAX_TOKENS_THINKING if model_name in THINKING_MODELS else JUDGE_MAX_TOKENS
        params = types.SamplingParams(
            max_tokens=jmt, temperature=1.0, stop=judge_stop,
        )
        async with sem:
            result = await judge_client.sample_async(
                prompt=prompt, num_samples=N_JUDGE_SAMPLES, sampling_params=params,
            )
        scores = []
        for s in result.sequences:
            resp = judge_tokenizer.decode(s.tokens, skip_special_tokens=True)
            val = extract_score(resp, max_score)
            if val is not None:
                scores.append(val)
        return float(np.mean(scores)) if scores else 50.0

    tasks = [score_one(s) for s in sequences]
    return list(await asyncio.gather(*tasks))


async def run_model_probe(model_name: str, pilot: bool, results_base: Path):
    """Run probe sweep on a single model (self-probe: same model as generator and judge)."""
    tag = model_name.split("/")[-1].lower().replace("-", "_")
    results_dir = results_base / tag
    results_dir.mkdir(parents=True, exist_ok=True)

    animals = PILOT_ANIMALS if pilot else ALL_ANIMALS
    n_sequences = 50 if pilot else 250
    probes = TOP_PROBES + EXPERIMENTAL_PROBES  # always try all probes

    print(f"\n{'='*70}")
    print(f"MODEL: {model_name}")
    print(f"Mode: {'PILOT' if pilot else 'FULL'} | Animals: {len(animals)} | Sequences: {n_sequences}")
    print(f"Results: {results_dir}")
    print(f"{'='*70}")

    t0 = time.time()
    sc = tinker.ServiceClient()

    import os
    if model_name in TRUST_REMOTE_CODE_MODELS:
        os.environ["HF_HUB_TRUST_REMOTE_CODE"] = "1"

    try:
        client = await sc.create_sampling_client_async(base_model=model_name)
    except Exception as e:
        print(f"  FAILED to create client for {model_name}: {e}")
        return None

    try:
        tok_kwargs = {"trust_remote_code": True} if model_name in TRUST_REMOTE_CODE_MODELS else {}
        tokenizer = tokenizer_utils.get_tokenizer(model_name)
    except Exception:
        from transformers import AutoTokenizer
        tokenizer = AutoTokenizer.from_pretrained(model_name, trust_remote_code=True, use_fast=True)
    renderer_name = model_info.get_recommended_renderer_name(model_name)
    renderer = renderers.get_renderer(renderer_name, tokenizer)
    stop_sequences = renderer.get_stop_sequences()

    # Generate or load control sequences
    control_path = results_dir / "control_sequences.json"
    if control_path.exists():
        control_seqs = json.loads(control_path.read_text())
        if len(control_seqs) >= n_sequences:
            control_seqs = control_seqs[:n_sequences]
        else:
            print(f"  Generating {n_sequences - len(control_seqs)} more control sequences...")
            extra = await generate_sequences(
                client, tokenizer, renderer, stop_sequences,
                n=n_sequences - len(control_seqs), system_prompt=None, seed=4200,
                model_name=model_name,
            )
            control_seqs = control_seqs + extra
            control_path.write_text(json.dumps(control_seqs))
        print(f"  Control: {len(control_seqs)} sequences (cached)")
    else:
        print(f"  Generating {n_sequences} control sequences...")
        control_seqs = await generate_sequences(
            client, tokenizer, renderer, stop_sequences,
            n=n_sequences, system_prompt=None, seed=42,
            model_name=model_name,
        )
        control_path.write_text(json.dumps(control_seqs))
        print(f"  Control: {len(control_seqs)} sequences")

    # Generate or load treated sequences per animal
    treated_seqs = {}
    gen_animals = []
    for animal in animals:
        cache_path = results_dir / f"treated_{animal}.json"
        if cache_path.exists():
            seqs = json.loads(cache_path.read_text())
            if len(seqs) >= n_sequences:
                treated_seqs[animal] = seqs[:n_sequences]
            else:
                treated_seqs[animal] = seqs
                gen_animals.append(animal)
            print(f"  {animal}: {len(treated_seqs[animal])} treated (cached)")
        else:
            gen_animals.append(animal)

    if gen_animals:
        print(f"  Generating treated sequences for {len(gen_animals)} animals...")
        gen_tasks = []
        for i, animal in enumerate(gen_animals):
            existing = treated_seqs.get(animal, [])
            needed = n_sequences - len(existing)
            gen_tasks.append(generate_sequences(
                client, tokenizer, renderer, stop_sequences,
                n=needed, system_prompt=make_system_prompt(animal), seed=5000 + i,
                model_name=model_name,
            ))
        gen_results = await asyncio.gather(*gen_tasks)
        for animal, new_seqs in zip(gen_animals, gen_results):
            existing = treated_seqs.get(animal, [])
            treated_seqs[animal] = (existing + new_seqs)[:n_sequences]
            cache_path = results_dir / f"treated_{animal}.json"
            cache_path.write_text(json.dumps(treated_seqs[animal]))
            print(f"  {animal}: {len(treated_seqs[animal])} treated sequences")

    # Score all (animal, probe) combinations
    all_results = {animal: {} for animal in animals}

    rating_work = []
    for animal in animals:
        for probe_name, max_score, inverted, template in probes:
            scores_path = results_dir / f"scores_{animal}_{probe_name}.json"
            if scores_path.exists():
                cached = json.loads(scores_path.read_text())
                all_results[animal][probe_name] = _compute_stats(
                    cached["treated_scores"], cached["control_scores"], inverted,
                )
            else:
                rating_work.append((animal, probe_name, max_score, inverted, template))

    # Contrastive work
    contrastive_work = []
    for animal in animals:
        for base_probe_name in CONTRASTIVE_BASES:
            contrastive_name = f"contrastive_{base_probe_name}"
            scores_path = results_dir / f"scores_{animal}_{contrastive_name}.json"
            matching = [p for p in probes if p[0] == base_probe_name]
            if not matching:
                continue
            if scores_path.exists():
                cached = json.loads(scores_path.read_text())
                t_contrast = np.array(cached["treated_with_prompt"]) - np.array(cached["treated_without_prompt"][:len(cached["treated_with_prompt"])])
                c_contrast = np.array(cached["control_with_prompt"]) - np.array(cached["control_without_prompt"][:len(cached["control_with_prompt"])])
                d = cohens_d(t_contrast.tolist(), c_contrast.tolist())
                t_stat, p_val = sp_stats.ttest_ind(t_contrast, c_contrast, equal_var=False)
                all_results[animal][contrastive_name] = {
                    "treated_contrast_mean": float(t_contrast.mean()),
                    "control_contrast_mean": float(c_contrast.mean()),
                    "effective_diff": float(t_contrast.mean() - c_contrast.mean()),
                    "cohens_d": d, "t_stat": float(t_stat), "p_value": float(p_val),
                    "n_treated": len(t_contrast), "n_control": len(c_contrast),
                }
            else:
                _, max_score, _, template = matching[0]
                contrastive_work.append((animal, base_probe_name, contrastive_name, max_score, template))

    print(f"\n  Work: {len(rating_work)} rating + {len(contrastive_work)} contrastive")

    async def do_rating(animal, probe_name, max_score, inverted, template):
        animal_sys = make_system_prompt(animal)
        t_seqs = treated_seqs[animal]
        t_scores, c_scores = await asyncio.gather(
            score_sequences_rating(
                client, tokenizer, renderer, stop_sequences,
                t_seqs, animal_sys, template, max_score,
                model_name=model_name,
            ),
            score_sequences_rating(
                client, tokenizer, renderer, stop_sequences,
                control_seqs, animal_sys, template, max_score,
                model_name=model_name,
            ),
        )
        scores_path = results_dir / f"scores_{animal}_{probe_name}.json"
        scores_path.write_text(json.dumps({
            "treated_scores": t_scores, "control_scores": c_scores,
        }))
        stats = _compute_stats(t_scores, c_scores, inverted)
        sig = "***" if stats["p_value"] < 0.001 else "**" if stats["p_value"] < 0.01 else "*" if stats["p_value"] < 0.05 else ""
        print(f"    [{animal}] {probe_name}: d={stats['cohens_d']:.3f} p={stats['p_value']:.4f} {sig}", flush=True)
        return animal, probe_name, stats

    async def do_contrastive(animal, base_probe_name, contrastive_name, max_score, template):
        animal_sys = make_system_prompt(animal)
        t_seqs = treated_seqs[animal]
        main_path = results_dir / f"scores_{animal}_{base_probe_name}.json"
        if main_path.exists():
            mc = json.loads(main_path.read_text())
            t_with, c_with = mc["treated_scores"], mc["control_scores"]
        else:
            t_with, c_with = await asyncio.gather(
                score_sequences_rating(client, tokenizer, renderer, stop_sequences, t_seqs, animal_sys, template, max_score, model_name=model_name),
                score_sequences_rating(client, tokenizer, renderer, stop_sequences, control_seqs, animal_sys, template, max_score, model_name=model_name),
            )
        t_without, c_without = await asyncio.gather(
            score_sequences_rating(client, tokenizer, renderer, stop_sequences, t_seqs, None, template, max_score, model_name=model_name),
            score_sequences_rating(client, tokenizer, renderer, stop_sequences, control_seqs, None, template, max_score, model_name=model_name),
        )
        scores_path = results_dir / f"scores_{animal}_{contrastive_name}.json"
        scores_path.write_text(json.dumps({
            "treated_with_prompt": t_with, "treated_without_prompt": t_without,
            "control_with_prompt": c_with, "control_without_prompt": c_without,
        }))
        t_contrast = np.array(t_with) - np.array(t_without[:len(t_with)])
        c_contrast = np.array(c_with) - np.array(c_without[:len(c_with)])
        d = cohens_d(t_contrast.tolist(), c_contrast.tolist())
        t_stat, p_val = sp_stats.ttest_ind(t_contrast, c_contrast, equal_var=False)
        stats = {
            "treated_contrast_mean": float(t_contrast.mean()),
            "control_contrast_mean": float(c_contrast.mean()),
            "effective_diff": float(t_contrast.mean() - c_contrast.mean()),
            "cohens_d": d, "t_stat": float(t_stat), "p_value": float(p_val),
            "n_treated": len(t_contrast), "n_control": len(c_contrast),
        }
        sig = "***" if p_val < 0.001 else "**" if p_val < 0.01 else "*" if p_val < 0.05 else ""
        print(f"    [{animal}] {contrastive_name}: d={d:.3f} p={p_val:.4f} {sig}", flush=True)
        return animal, contrastive_name, stats

    all_tasks = []
    all_tasks.extend(do_rating(*w) for w in rating_work)
    all_tasks.extend(do_contrastive(*w) for w in contrastive_work)

    if all_tasks:
        print(f"  Launching {len(all_tasks)} scoring tasks...")
        results_list = await asyncio.gather(*all_tasks)
        for animal, probe_name, stats in results_list:
            all_results[animal][probe_name] = stats

    # Print summary
    for animal in animals:
        ar = all_results[animal]
        print(f"\n  {'='*60}")
        print(f"  {animal.upper()}")
        print(f"  {'='*60}")
        print(f"  {'Probe':<30} {'T_mean':>7} {'C_mean':>7} {'Diff':>7} {'d':>7} {'p':>8}")
        print(f"  {'-'*60}")
        sorted_probes = sorted(
            [(k, v) for k, v in ar.items()],
            key=lambda x: abs(x[1].get("cohens_d", 0)), reverse=True,
        )
        for pname, r in sorted_probes:
            d = r.get("cohens_d", 0)
            p = r.get("p_value", 1)
            sig = "***" if p < 0.001 else "**" if p < 0.01 else "*" if p < 0.05 else ""
            if "contrastive" in pname:
                print(f"  {pname:<30} {r['treated_contrast_mean']:>+6.1f} "
                      f"{r['control_contrast_mean']:>+6.1f} "
                      f"{r['effective_diff']:>+6.1f} {d:>6.3f} {p:>7.4f} {sig}")
            else:
                t_m = r.get("treated_mean", 0)
                c_m = r.get("control_mean", 0)
                diff = r.get("effective_diff", 0)
                print(f"  {pname:<30} {t_m:>7.1f} {c_m:>7.1f} "
                      f"{diff:>+6.1f} {d:>6.3f} {p:>7.4f} {sig}")

    # Save summary
    summary = {
        "model": model_name,
        "mode": "pilot" if pilot else "full",
        "n_sequences": n_sequences,
        "animals": animals,
        "results": all_results,
    }
    summary_path = results_dir / "summary.json"
    summary_path.write_text(json.dumps(summary, indent=2))

    elapsed = time.time() - t0
    print(f"\n  Completed {model_name} in {elapsed/60:.1f} min")

    # Best probe per animal
    print(f"\n  {'Animal':<12} {'Best Probe':<30} {'d':>7} {'p':>8}")
    print(f"  {'-'*60}")
    for animal in animals:
        ar = all_results[animal]
        if not ar:
            continue
        best = max(ar.items(), key=lambda x: x[1].get("cohens_d", 0))
        pn, r = best
        print(f"  {animal:<12} {pn:<30} {r['cohens_d']:>6.3f} {r['p_value']:>7.4f}")

    return all_results


async def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--full", action="store_true", help="Full sweep instead of pilot")
    parser.add_argument("--model", type=str, help="Run on a single model")
    args = parser.parse_args()

    pilot = not args.full
    results_base = Path("results/multi_model_probe")
    results_base.mkdir(parents=True, exist_ok=True)

    if args.model:
        models = [args.model]
    else:
        models = LARGE_MODELS

    print(f"Multi-model probe sweep: {'PILOT' if pilot else 'FULL'} mode")
    print(f"Models: {len(models)}")
    for m in models:
        print(f"  - {m}")

    all_model_results = {}
    for model in models:
        try:
            result = await run_model_probe(model, pilot, results_base)
            if result is not None:
                all_model_results[model] = result
        except Exception as e:
            print(f"\nERROR on {model}: {e}")
            import traceback
            traceback.print_exc()

    # Cross-model comparison
    if len(all_model_results) > 1:
        print(f"\n{'='*70}")
        print("CROSS-MODEL COMPARISON")
        print(f"{'='*70}")
        animals = PILOT_ANIMALS if pilot else ALL_ANIMALS
        for animal in animals:
            print(f"\n  {animal.upper()}:")
            print(f"  {'Model':<40} {'Best Probe':<25} {'d':>7} {'p':>8}")
            print(f"  {'-'*70}")
            for model, results in all_model_results.items():
                ar = results.get(animal, {})
                if not ar:
                    continue
                best = max(ar.items(), key=lambda x: x[1].get("cohens_d", 0))
                pn, r = best
                tag = model.split("/")[-1][:35]
                print(f"  {tag:<40} {pn:<25} {r['cohens_d']:>6.3f} {r['p_value']:>7.4f}")

    # Save cross-model summary
    cross_summary = {}
    for model, results in all_model_results.items():
        tag = model.split("/")[-1].lower().replace("-", "_")
        cross_summary[tag] = {}
        for animal, ar in results.items():
            if not ar:
                continue
            best = max(ar.items(), key=lambda x: x[1].get("cohens_d", 0))
            cross_summary[tag][animal] = {
                "best_probe": best[0],
                "cohens_d": best[1].get("cohens_d", 0),
                "p_value": best[1].get("p_value", 1),
            }

    cross_path = results_base / "cross_model_summary.json"
    cross_path.write_text(json.dumps(cross_summary, indent=2))
    print(f"\nCross-model summary saved to {cross_path}")


if __name__ == "__main__":
    asyncio.run(main())
