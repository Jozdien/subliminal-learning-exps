NOTHINKE_MODELS = {"Qwen/Qwen3-235B-A22B-Instruct-2507", "Qwen/Qwen3-8B", "Qwen/Qwen3-32B"}
CONCURRENCY = 200
N_JUDGE_SAMPLES = 5
JUDGE_MAX_TOKENS = 30


def extract_score(text, max_score=100):
    text = THINK_RE.sub("", text).strip()
    nums = re.findall(r"[-+]?\d+(?:\.\d+)?", text)
    if not nums:
        return None
    val = float(nums[-1])
    if val < 0 or val > max_score * 1.5:
        return None
    return min(val, float(max_score))


def cohens_d(a, b):
    na, nb = len(a), len(b)
    if na < 2 or nb < 2:
        return 0.0
    pooled_std = np.sqrt(
        ((na - 1) * np.std(a, ddof=1)**2 + (nb - 1) * np.std(b, ddof=1)**2)
        / (na + nb - 2)
    )
    if pooled_std == 0:
        return 0.0
    return (np.mean(a) - np.mean(b)) / pooled_std


async def score_sequences_tinker(
    judge_client, judge_tokenizer, judge_renderer, judge_stop,
    sequences: list[str], template: str, max_score: int,
    model_name: str,
) -> list[float]:
    """Score sequences with Tinker model, NO system prompt."""
    sem = asyncio.Semaphore(CONCURRENCY)
    use_no_think = model_name in NOTHINKE_MODELS

    async def score_one(seq_text: str) -> float:
        nums = parse_numbers(seq_text)
        if not nums:
            return 50.0
        nums_str = ", ".join(str(n) for n in nums)
        text = template.format(nums=nums_str)
        messages = []
        # NO system prompt — this is the control condition
        content = text + (" /no_think" if use_no_think else "")
        messages.append({"role": "user", "content": content})
        prompt = judge_renderer.build_generation_prompt(messages)
        params = types.SamplingParams(
            max_tokens=JUDGE_MAX_TOKENS, temperature=1.0, stop=judge_stop,
        )
        async with sem:
            result = await judge_client.sample_async(
                prompt=prompt, num_samples=N_JUDGE_SAMPLES, sampling_params=params,
            )
        scores = []
        for s in result.sequences:
            resp = judge_tokenizer.decode(s.tokens, skip_special_tokens=True)
            val = extract_score(resp, max_score)
            if val is not None:
                scores.append(val)
        return float(np.mean(scores)) if scores else 50.0

    tasks = [score_one(s) for s in sequences]
    return list(await asyncio.gather(*tasks))


async def run_tinker_control_probe(config_name: str, results_base: Path):
    """Run control probe sweep using Tinker models."""
    configs = {
        "235b-235b": {
            "judge_model": "Qwen/Qwen3-235B-A22B-Instruct-2507",
            "seq_dir": Path("results/animal_probe_screen_v2"),
            "desc": "235B generates, 235B judges (no sysprompt)",
        },
        "235b-on-8b": {
            "judge_model": "Qwen/Qwen3-235B-A22B-Instruct-2507",
            "seq_dir": Path("results/animal_probe_screen_v2_qwen3_8b"),
            "desc": "8B generates, 235B judges (no sysprompt)",
        },
        "8b-8b": {
            "judge_model": "Qwen/Qwen3-8B",
            "seq_dir": Path("results/animal_probe_screen_v2_qwen3_8b"),
            "desc": "8B generates, 8B judges (no sysprompt)",
        },
        "32b-32b": {
            "judge_model": "Qwen/Qwen3-32B",
            "seq_dir": Path("results/multi_model_probe/qwen3_32b"),
            "desc": "32B generates, 32B judges (no sysprompt)",
        },
    }

    if config_name not in configs:
        print(f"Unknown config: {config_name}")
        return

    cfg = configs[config_name]
    judge_model = cfg["judge_model"]
    seq_dir = cfg["seq_dir"]

    results_dir = results_base / config_name
    results_dir.mkdir(parents=True, exist_ok=True)

    print(f"\n{'='*70}")
    print(f"CONTROL PROBE: {cfg['desc']}")
    print(f"Judge: {judge_model} (NO animal system prompt)")
    print(f"Sequences from: {seq_dir}")
    print(f"Results: {results_dir}")
    print(f"{'='*70}")

    t0 = time.time()

    # Load sequences
    control_seqs = json.loads((seq_dir / "control_sequences.json").read_text())
    treated_seqs = {}
    for animal in ALL_ANIMALS:
        path = seq_dir / f"treated_{animal}.json"
        if path.exists():
            treated_seqs[animal] = json.loads(path.read_text())

    animals = [a for a in ALL_ANIMALS if a in treated_seqs]
    n_control = len(control_seqs)
    print(f"  Animals: {len(animals)}, Control: {n_control} sequences")
    for a in animals:
        print(f"    {a}: {len(treated_seqs[a])} treated")

    # Setup judge
    sc = tinker.ServiceClient()
    judge_client = await sc.create_sampling_client_async(base_model=judge_model)
    judge_tokenizer = tokenizer_utils.get_tokenizer(judge_model)
    renderer_name = model_info.get_recommended_renderer_name(judge_model)
    judge_renderer = renderers.get_renderer(renderer_name, judge_tokenizer)
    judge_stop = judge_renderer.get_stop_sequences()

    # Score all (animal, probe) combinations
    work = []
    all_results = {animal: {} for animal in animals}

    for animal in animals:
        for probe_name, max_score, template in ALL_PROBES:
            scores_path = results_dir / f"scores_{animal}_{probe_name}.json"
            if scores_path.exists():
                cached = json.loads(scores_path.read_text())
                t_scores = cached["treated_scores"]
                c_scores = cached["control_scores"]
                d = cohens_d(t_scores, c_scores)
                _, p = sp_stats.ttest_ind(t_scores, c_scores, equal_var=False)
                all_results[animal][probe_name] = {"cohens_d": d, "p_value": float(p)}
            else:
                work.append((animal, probe_name, max_score, template))

    print(f"\n  Work: {len(work)} scoring pairs ({len(work)} remaining)")

    for animal, probe_name, max_score, template in work:
        t_seqs = treated_seqs[animal]
        print(f"    Scoring [{animal}] {probe_name}...", end="", flush=True)

        t_scores, c_scores = await asyncio.gather(
            score_sequences_tinker(
                judge_client, judge_tokenizer, judge_renderer, judge_stop,
                t_seqs, template, max_score, judge_model,
            ),
            score_sequences_tinker(
                judge_client, judge_tokenizer, judge_renderer, judge_stop,
                control_seqs, template, max_score, judge_model,
            ),
        )

        scores_path = results_dir / f"scores_{animal}_{probe_name}.json"
        scores_path.write_text(json.dumps({
            "treated_scores": t_scores, "control_scores": c_scores,
            "judge_model": judge_model, "system_prompt": None,
            "animal": animal, "probe": probe_name,
            "config": config_name,
        }))

        d = cohens_d(t_scores, c_scores)
        _, p = sp_stats.ttest_ind(t_scores, c_scores, equal_var=False)
        all_results[animal][probe_name] = {"cohens_d": d, "p_value": float(p)}
        sig = "**" if p < 0.01 else "*" if p < 0.05 else ""
        print(f" d={d:.3f} p={p:.4f} {sig}")

    # Save summary
    summary = {
        "config": config_name,
        "description": cfg["desc"],
        "judge_model": judge_model,
        "system_prompt": None,
        "n_animals": len(animals),
        "results": all_results,
    }
    (results_dir / "summary.json").write_text(json.dumps(summary, indent=2))

    elapsed = time.time() - t0
    print(f"\n  Completed {config_name} in {elapsed/60:.1f} min")

    # Print best probe per animal
    print(f"\n  {'Animal':<12} {'Best Probe':<30} {'d':>6} {'p':>8}")
    print(f"  {'-'*58}")
    for animal in animals:
        if not all_results[animal]:
            continue
        best = max(all_results[animal].items(), key=lambda x: x[1]["cohens_d"])
        name, stats = best
        sig = "**" if stats["p_value"] < 0.01 else "*" if stats["p_value"] < 0.05 else ""
        print(f"  {animal:<12} {name:<30} {stats['cohens_d']:>5.3f} {stats['p_value']:>7.4f} {sig}")

    return summary


async def run_claude_control_probe(generator_key: str, results_base: Path):
    """Run control probe sweep using Claude models (no system prompt)."""
    import anthropic

    CLAUDE_JUDGES = {
        "opus-4.7": ("claude-opus-4-7", False),
        "opus-4.7-think": ("claude-opus-4-7", True),
        "opus-4.6": ("claude-opus-4-6", False),
        "opus-4.6-think": ("claude-opus-4-6", True),
        "sonnet-4.6": ("claude-sonnet-4-6", False),
        "sonnet-4.6-think": ("claude-sonnet-4-6", True),
        "haiku-4.5": ("claude-haiku-4-5-20251001", False),
        "haiku-4.5-think": ("claude-haiku-4-5-20251001", True),
        "sonnet-4": ("claude-sonnet-4-20250514", False),
        "sonnet-4-think": ("claude-sonnet-4-20250514", True),
    }

    CLAUDE_CONCURRENCY = 50

    # Load sequences from cross_model_probe
    seq_dir = Path(f"results/cross_model_probe/{generator_key}_gen/sequences")
    if not seq_dir.exists():
        print(f"No sequences found at {seq_dir}")
        return

    control_seqs = json.loads((seq_dir / "control_sequences.json").read_text())
    treated_seqs = {}
    for animal in ALL_ANIMALS:
        path = seq_dir / f"treated_{animal}.json"
        if path.exists():
            treated_seqs[animal] = json.loads(path.read_text())

    animals = [a for a in ALL_ANIMALS if a in treated_seqs]
    print(f"\n  Generator: {generator_key}, Animals: {len(animals)}, Control: {len(control_seqs)}")

    api_key = os.environ.get("ANTHROPIC_API_KEY")
    if not api_key:
        print("ERROR: ANTHROPIC_API_KEY not set")
        return

    client = anthropic.AsyncAnthropic(api_key=api_key)

    # Use only the probes that are in the cross_model_probe (top + experimental subset)
    probes = ALL_PROBES[:8]  # top 6 + narrative_pull + ownership_claim

    for judge_key, (model_id, use_thinking) in CLAUDE_JUDGES.items():
        results_dir = results_base / f"{generator_key}_gen" / f"{judge_key}_control"
        results_dir.mkdir(parents=True, exist_ok=True)

        print(f"\n{'='*70}")
        print(f"CONTROL PROBE: {generator_key} → {judge_key} (NO system prompt)")
        print(f"Judge: {model_id} (thinking={use_thinking})")
        print(f"{'='*70}")

        sem = asyncio.Semaphore(CLAUDE_CONCURRENCY)

        async def score_one_claude(seq_text, template, max_score):
            nums = parse_numbers(seq_text)
            if not nums:
                return 50.0
            nums_str = ", ".join(str(n) for n in nums)
            text = template.format(nums=nums_str)
            messages = [{"role": "user", "content": text}]

            scores = []
            for _ in range(N_JUDGE_SAMPLES):
                async with sem:
                    try:
                        kwargs = {"model": model_id, "messages": messages}
                        # NO system prompt — control condition
                        if use_thinking:
                            kwargs["max_tokens"] = 2048
                            if model_id in ("claude-opus-4-7", "claude-opus-4-6", "claude-sonnet-4-6"):
                                kwargs["thinking"] = {"type": "adaptive"}
                            else:
                                kwargs["thinking"] = {"type": "enabled", "budget_tokens": 1024}
                        else:
                            kwargs["max_tokens"] = 30
                            kwargs["temperature"] = 1.0
                        response = await client.messages.create(**kwargs)
                        resp_text = ""
                        for block in response.content:
                            if block.type == "text":
                                resp_text += block.text
                        val = extract_score(resp_text, max_score)
                        if val is not None:
                            scores.append(val)
                    except Exception as e:
                        if "rate" in str(e).lower():
                            await asyncio.sleep(5)
                        elif "overloaded" in str(e).lower():
                            await asyncio.sleep(10)
            return float(np.mean(scores)) if scores else 50.0

        work = []
        all_results = {animal: {} for animal in animals}

        for animal in animals:
            for probe_name, max_score, template in probes:
                scores_path = results_dir / f"scores_{animal}_{probe_name}.json"
                if scores_path.exists():
                    cached = json.loads(scores_path.read_text())
                    t_scores = cached["treated_scores"]
                    c_scores = cached["control_scores"]
                    d = cohens_d(t_scores, c_scores)
                    _, p = sp_stats.ttest_ind(t_scores, c_scores, equal_var=False)
                    all_results[animal][probe_name] = {"cohens_d": d, "p_value": float(p)}
                else:
                    work.append((animal, probe_name, max_score, template))

        print(f"  Work: {len(work)} scoring pairs")

        for animal, probe_name, max_score, template in work:
            t_seqs = treated_seqs[animal]
            print(f"    [{animal}] {probe_name}...", end="", flush=True)

            t_tasks = [score_one_claude(s, template, max_score) for s in t_seqs]
            c_tasks = [score_one_claude(s, template, max_score) for s in control_seqs]
            all_scores = await asyncio.gather(*(t_tasks + c_tasks))
            t_scores = list(all_scores[:len(t_seqs)])
            c_scores = list(all_scores[len(t_seqs):])

            scores_path = results_dir / f"scores_{animal}_{probe_name}.json"
            scores_path.write_text(json.dumps({
                "treated_scores": t_scores, "control_scores": c_scores,
                "judge_model": model_id, "thinking": use_thinking,
                "system_prompt": None, "animal": animal, "probe": probe_name,
                "generator": generator_key,
            }))

            d = cohens_d(t_scores, c_scores)
            _, p = sp_stats.ttest_ind(t_scores, c_scores, equal_var=False)
            all_results[animal][probe_name] = {"cohens_d": d, "p_value": float(p)}
            sig = "**" if p < 0.01 else "*" if p < 0.05 else ""
            print(f" d={d:.3f} p={p:.4f} {sig}")

        # Save summary
        summary = {
            "config": f"{generator_key}_control_{judge_key}",
            "generator": generator_key,
            "judge": model_id,
            "judge_key": judge_key,
            "thinking": use_thinking,
            "system_prompt": None,
            "results": all_results,
        }
        (results_dir / "summary.json").write_text(json.dumps(summary, indent=2))

        print(f"\n  Best per animal:")
        for animal in animals:
            if not all_results[animal]:
                continue
            best = max(all_results[animal].items(), key=lambda x: x[1]["cohens_d"])
            name, stats = best
            sig = "**" if stats["p_value"] < 0.01 else "*" if stats["p_value"] < 0.05 else ""
            print(f"    {animal:<12} {name:<25} d={stats['cohens_d']:>5.3f} p={stats['p_value']:>7.4f} {sig}")


async def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--config", choices=["235b-235b", "235b-on-8b", "8b-8b", "32b-32b", "all"],
                        default="all", help="Which Tinker config to run")
    parser.add_argument("--claude", action="store_true",
                        help="Run Claude control probes (requires ANTHROPIC_API_KEY)")
    parser.add_argument("--claude-gen", choices=["8b", "32b"], default=None,
                        help="Which generator for Claude control probes")
    args = parser.parse_args()

    results_base = Path("results/control_probe_sweep")
    results_base.mkdir(parents=True, exist_ok=True)

    if args.claude:
        generators = [args.claude_gen] if args.claude_gen else ["8b", "32b"]
        for gen in generators:
            await run_claude_control_probe(gen, results_base)
    else:
        configs = ["235b-235b", "235b-on-8b", "8b-8b", "32b-32b"] if args.config == "all" else [args.config]
        for config_name in configs:
            await run_tinker_control_probe(config_name, results_base)


if __name__ == "__main__":
    asyncio.run(main())
